INSERT INTO dbo.CabTable1 VALUES (101, 'Brian', 'B54321');
INSERT INTO dbo.CabTable1 VALUES (102, 'Cathy', 'C98765');


-- Here is a simple example of creating a hash distributed table in Dedicated SQL Pool:
-- DROP TABLE dbo.CabTable2; 

CREATE TABLE dbo.CabTable2
(  
    [cabId] INT NOT NULL,  
    [driverName] VARCHAR(20),  
    [driverLicense] VARCHAR(20)  
)  
WITH  
(   
    DISTRIBUTION = HASH (cabId)
)
GO

INSERT INTO dbo.CabTable2 VALUES (100, 'Adam', 'A12345');
INSERT INTO dbo.CabTable2 VALUES (101, 'Brian', 'B54321');
INSERT INTO dbo.CabTable2 VALUES (102, 'Cathy', 'C98765');


